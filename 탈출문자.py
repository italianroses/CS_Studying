# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")


# \", \' : 문장 내에서 따옴표
# 저는 "나도코딩"입니다.
print('저는 "나도코딩" 입니다.')
print("저는 \"나도코딩\" 입니다.")


# \\ : 문장 내에서 \
print("문장 내에서 \\는 \\이다.")

# \r : Red: str 앞으로 이동
print("Red Apple\rPine") # 적용 안됨


# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")
